package py.una.sgf.registros;

public class PaqueteJSON {

	private int ancho;// z
	private int alto;// y
	private int largo;// x
	private int x;
	private int y;
	private int z;
	private String color;

	public int getAncho() {

		return ancho;
	}

	public void setAncho(int ancho) {

		this.ancho = ancho;
	}

	public int getAlto() {

		return alto;
	}

	public void setAlto(int alto) {

		this.alto = alto;
	}

	public int getLargo() {

		return largo;
	}

	public void setLargo(int largo) {

		this.largo = largo;
	}

	public int getX() {

		return x;
	}

	public void setX(int x) {

		this.x = x;
	}

	public int getY() {

		return y;
	}

	public void setY(int y) {

		this.y = y;
	}

	public int getZ() {

		return z;
	}

	public void setZ(int z) {

		this.z = z;
	}

	public String getColor() {

		return color;
	}

	public void setColor(String color) {

		this.color = color;
	}

}
